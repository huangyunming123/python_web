def get_baidu_coordinates(query):
    """调用百度地图API获取坐标"""
    params = {
        "query": query,
        "region": "兰考县",  # 限定兰考县区域
        "output": "json",
        "ak": API_KEY
    }
    try:
        response = requests.get(API_URL, params=params)
        data = response.json()

        if data.get("status") == 0 and data.get("results"):
            result = data["results"][0]
            return {
                "网点名称": query,
                "经度(lng)": result["location"]["lng"],
                "纬度(lat)": result["location"]["lat"],
                "详细地址": result.get("address", ""),
                "百度地图UID": result.get("uid", "")
            }
        else:
            print(f"未找到: {query} | 错误信息: {data.get('message')}")
            return None
    except Exception as e:
        print(f"请求失败: {query} | 错误: {str(e)}")
        return None


